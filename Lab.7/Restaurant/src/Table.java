public class Table {

    private int numberOfTable;
    private String placeOfTable;

    public Table(int numberOfTable , String placeOfTable) {
        this.numberOfTable = numberOfTable;
        this.placeOfTable = placeOfTable;
    }

    public int getNumberOfTable() {
        return numberOfTable;
    }

    public String getPlaceOfTable() {
        return placeOfTable;
    }
}
